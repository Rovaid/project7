$(function() {

let StepCount = 8;

for(let i = 0; i < StepCount; i++) {
  appendData(i);
}


$('#viewMore').click(function(e) {
  e.preventDefault();

  let start = StepCount;
  StepCount += 8;

  if(creatorsArray[start] !== undefined) {

    for(let i = start; i < StepCount; i++) {
      appendData(i);

      if(creatorsArray[i+1] == undefined) {
        $('#viewMore').html('<img src="images/arrow.png" alt="" class="fa-rotate-180" height="60px"> <br> <span class="font-bold">More is Coming</span>');
      }
    }
  }
});

});


